def _load_env() -> dict[str, str]:
    result: dict[str, str] = {}
    if not ENV_FILE.is_file():
        return result
    with open(ENV_FILE, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if "=" in line and not line.startswith("#"):
                k, _, v = line.partition("=")
                k, v = k.strip(), v.strip().strip("'\"")
                if k and v:
                    result[k] = v
    return result


