        bg_hint = (sug.get("hint") or "").strip()
        print(
            f"  渐变顶 RGB {bg_top} -> 底 RGB {bg_bottom}"
            + (f"；提示: {bg_hint}" if bg_hint else ""),
            flush=True,
        )
