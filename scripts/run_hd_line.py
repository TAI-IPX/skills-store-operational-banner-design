def _check_and_fix_cutout_integrity(
    cutout_path: Path,
    run_dir: Path,
    index: int,
    total: int,
) -> Path:
    """层1：检测单张抠图主体完整性，残缺则 micugpt2 inpaint 补全。返回最终路径。"""
    gsd = _import_gemini_detect()
    text = gsd._call_micugpt2_vision(str(cutout_path), _INTEGRITY_CHECK_PROMPT)
    if not text:
        print(f"  [HD 2a-ck/{total}] {cutout_path.name} Vision 检测失败，保留原图", flush=True)
        return cutout_path

    start = text.find("{")
    if start < 0:
        print(f"  [HD 2a-ck/{total}] {cutout_path.name} 无法解析 JSON，保留原图", flush=True)
        return cutout_path

    try:
        depth = 0
        for i, c in enumerate(text[start:], start):
            if c == "{":
                depth += 1
            elif c == "}":
                depth -= 1
                if depth == 0:
                    obj = json.loads(text[start : i + 1])
                    break
        else:
            print(f"  [HD 2a-ck/{total}] {cutout_path.name} JSON 截取失败，保留原图", flush=True)
            return cutout_path
    except (json.JSONDecodeError, UnboundLocalError):
        print(f"  [HD 2a-ck/{total}] {cutout_path.name} JSON 解析失败，保留原图", flush=True)
        return cutout_path

    if obj.get("complete", True):
        body_parts = {k: v for k, v in obj.items() if k in ("head", "face", "hands", "body")}
        flag_str = " ".join(f"{k}={'✓' if v else '✗'}" for k, v in body_parts.items())
        print(f"  [HD 2a-ck/{total}] {cutout_path.name} 完整 ({flag_str})", flush=True)
        return cutout_path

    missing_desc = str(obj.get("missing", "") or "缺失部分")
    body_parts = {k: v for k, v in obj.items() if k in ("head", "face", "hands", "body")}
    flag_str = " ".join(f"{k}={'✓' if v else '✗'}" for k, v in body_parts.items())
    print(f"  [HD 2a-ck/{total}] {cutout_path.name} 残缺 ({flag_str}) — {missing_desc}", flush=True)

    fixed_path = run_dir / f"hd_cutout_fixed_{index:02d}.png"
