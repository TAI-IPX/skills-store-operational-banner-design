def _check_and_fix_i2i_integrity(
    i2i_path: Path,
    run_dir: Path,
) -> Path:
    """层2：检测图生图输出中的人物完整性，残缺则 micugpt2 inpaint 补全。"""
    gsd = _import_gemini_detect()
    text = gsd._call_micugpt2_vision(str(i2i_path), _I2I_INTEGRITY_PROMPT)
    if not text:
        print("  [HD 3-ck] 图生图 Vision 检测失败，跳过补全", flush=True)
        return i2i_path

    start = text.find("{")
    if start < 0:
        print("  [HD 3-ck] 图生图 Vision 返回非 JSON，跳过补全", flush=True)
        return i2i_path

    try:
        depth = 0
        for i, c in enumerate(text[start:], start):
            if c == "{":
                depth += 1
            elif c == "}":
                depth -= 1
                if depth == 0:
                    obj = json.loads(text[start : i + 1])
                    break
        else:
            print("  [HD 3-ck] 图生图 JSON 截取失败，跳过补全", flush=True)
            return i2i_path
    except (json.JSONDecodeError, UnboundLocalError):
        print("  [HD 3-ck] 图生图 JSON 解析失败，跳过补全", flush=True)
        return i2i_path

    if obj.get("complete", True):
        print("  [HD 3-ck] 图生图人物完整，跳过补全", flush=True)
        return i2i_path

    issues = str(obj.get("issues", "") or "残缺部位")
    print(f"  [HD 3-ck] 图生图人物残缺: {issues}", flush=True)

    fixed_path = run_dir / "hd_line_i2i_fixed.png"
    inpaint_prompt = _I2I_INPAINT_PROMPT.format(issues=issues)

    prepare_dir = str(PREPARE_SCRIPT.parent)
    if prepare_dir not in sys.path:
        sys.path.insert(0, prepare_dir)
    from prepare_background import _micugpt2_edit_image

    try:
        _micugpt2_edit_image(str(i2i_path), str(fixed_path), inpaint_prompt)
        if fixed_path.is_file() and fixed_path.stat().st_size > 0:
            import shutil as _shutil
            _shutil.copy2(str(fixed_path), str(i2i_path))
            print("  [HD 3-ck] 图生图补全完成，已覆盖原文件", flush=True)
            return i2i_path
    except Exception as e:
        print(f"  [HD 3-ck] 补全失败 ({e})，保留原图", flush=True)
    return i2i_path


def _expand_norm_bbox(
    bbox: tuple[float, float, float, float],
    margin: float = 0.06,
) -> tuple[float, float, float, float]:
    x0, y0, x1, y1 = bbox
    bw, bh = max(1e-6, x1 - x0), max(1e-6, y1 - y0)
    mx, my = bw * margin, bh * margin
    return (
        max(0.0, x0 - mx),
        max(0.0, y0 - my),
        min(1.0, x1 + mx),
        min(1.0, y1 + my),
    )


def _norm_bbox_to_pixel_crop(
    w: int,
    h: int,
    bbox: tuple[float, float, float, float],
) -> tuple[int, int, int, int]:
    x0, y0, x1, y1 = bbox
    x1p = max(0, min(w - 1, math.floor(x0 * w)))
    y1p = max(0, min(h - 1, math.floor(y0 * h)))
    x2p = min(w, max(x1p + 1, math.ceil(x1 * w)))
    y2p = min(h, max(y1p + 1, math.ceil(y1 * h)))
    if x2p <= x1p or y2p <= y1p:
        return 0, 0, w, h
    return x1p, y1p, x2p, y2p


def _vertical_gradient_rgba(
    w: int,
    h: int,
    top: tuple[int, int, int],
    bottom: tuple[int, int, int],
) -> "object":
    from PIL import Image

    try:
        import numpy as np

        fac = np.linspace(1.0, 0.0, h, dtype=np.float32)[:, np.newaxis]
        ta = np.array(top, dtype=np.float32)
        ba = np.array(bottom, dtype=np.float32)
        rows = fac * ta + (1.0 - fac) * ba
        arr = np.tile(rows[:, np.newaxis, :], (1, w, 1))
        arr = np.clip(arr, 0, 255).astype(np.uint8)
        return Image.fromarray(arr, mode="RGB").convert("RGBA")
    except ImportError:
        img = Image.new("RGB", (w, h))
        pix = img.load()
        tr, tg, tb = top
        br, bg_, bb = bottom
        for y in range(h):
            t = y / max(h - 1, 1)
            r = int(round(tr * (1 - t) + br * t))
            g = int(round(tg * (1 - t) + bg_ * t))
            b = int(round(tb * (1 - t) + bb * t))
            for x in range(w):
                pix[x, y] = (r, g, b)
        return img.convert("RGBA")


def _alpha_bbox(im: "object") -> tuple[int, int, int, int]:
    bb = im.split()[-1].getbbox()
    if bb is None:
        return (0, 0, im.size[0], im.size[1])
    return bb


def _compose_horizontal_layout(
    cutout_paths: list[Path],
    canvas_w: int,
    canvas_h: int,
    bg_top: tuple[int, int, int],
    bg_bottom: tuple[int, int, int],
    *,
    layout_classic: bool = False,
    fill_height_ratio: float = 0.82,
    max_width_ratio: float = 0.96,
    overlap_ratio: float = 0.055,
    y_stagger_ratio: float = 0.022,
    ground_y_ratio: float = 0.86,
    stagger_mode: str = "sides_up",
    safe_inner: tuple[int, int, int, int] | None = None,
) -> "object":
    """
    横排拼版（本地几何，非 Gemini）。

    safe_inner：可选 (x_min, y_min, x_max, y_max)，与 spec 安全区一致；人物行的宽高与居中均在该矩形内计算，
    再平移合成到完整 canvas_w×canvas_h 上（左右留白保留给底图）。

    layout_classic=False（默认新逻辑）：
      1) 逐张读入 RGBA，用 alpha getbbox 得内容高度 ch_raw；过矮则用整图高 ch_use 且 scale 封顶 scale_cap，
         使 target_content_h ≈ 内区高 * fill_height_ratio。
      2) 算相邻重叠 overlap_px ≈ mean(width) * overlap_ratio，总跨度 span；若 span > 内区宽 * max_width_ratio
         则对所有层乘同一 factor 缩小。
      3) 纵向：ground_y_ratio 定「地面」线，y = gy - alpha底边 - dy。stagger_mode：
         - sides_up：两侧 dy 大、中间 dy=0（旧版，两侧画布上更靠上）。
         - center_pyramid：中间 dy 最大、两侧小（美宣常见「中间略高、金字塔」+ 配合绘制顺序显前）。
      4) 横向：span 整体水平居中；3 张时绘制顺序 0→2→1，中间最后画、压在两侧之上（显前）。

    layout_classic=True：固定约 62% 内区高、88% 内区宽上限、固定 gap 横排，无重叠与脚底对齐逻辑。
    """
    from PIL import Image

    ims = [Image.open(p).convert("RGBA") for p in cutout_paths]
    if not ims:
        raise RuntimeError("拼版失败：没有有效抠图")

    sx0, sy0 = 0, 0
    vi_w, vi_h = canvas_w, canvas_h
    if safe_inner is not None:
        x0, y0, x1, y1 = safe_inner
        x0 = max(0, min(int(x0), canvas_w - 1))
        y0 = max(0, min(int(y0), canvas_h - 1))
        x1 = max(x0 + 1, min(int(x1), canvas_w))
        y1 = max(y0 + 1, min(int(y1), canvas_h))
        if x1 - x0 >= 64 and y1 - y0 >= 64:
            sx0, sy0, vi_w, vi_h = x0, y0, x1 - x0, y1 - y0

    if layout_classic:
        target_h = int(vi_h * 0.62)
        scaled: list = []
        for im in ims:
            w, h = im.size
            if h <= 0:
                continue
            nh = min(target_h, h)
            nw = max(1, int(w * nh / h))
            scaled.append(im.resize((nw, nh), Image.Resampling.LANCZOS))
        if not scaled:
            raise RuntimeError("拼版失败：没有有效抠图")
        gap = max(12, vi_w // 160)
        total_w = sum(im.size[0] for im in scaled) + gap * (len(scaled) - 1)
        max_allowed = int(vi_w * 0.88)
        if total_w > max_allowed:
            factor = max_allowed / total_w
            scaled = [
                im.resize(
                    (max(1, int(im.size[0] * factor)), max(1, int(im.size[1] * factor))),
                    Image.Resampling.LANCZOS,
                )
                for im in scaled
            ]
            total_w = sum(im.size[0] for im in scaled) + gap * (len(scaled) - 1)
        canvas = _vertical_gradient_rgba(canvas_w, canvas_h, bg_top, bg_bottom)
        max_sc_h = max(im.size[1] for im in scaled)
        x = sx0 + (vi_w - total_w) // 2
        y_base = sy0 + (vi_h - max_sc_h) // 2
        for im in scaled:
            y = y_base + (max_sc_h - im.size[1]) // 2
            canvas.alpha_composite(im, (x, y))
            x += im.size[0] + gap
        return canvas

    # —— 统一主体高度（按 alpha 内容框高度）——
    # 若 ch 极矮（碎块/条带），scale 会爆炸 → 整行超宽 → 统一缩小后人物变成小点；改用整图高并封顶 scale
    fh = max(0.35, min(0.95, float(fill_height_ratio)))
    target_content_h = max(8, int(vi_h * fh))
    alpha_h_min_ratio = 0.12
    scale_cap = 5.0
    scaled = []
    for im in ims:
        w, h = im.size
        if h <= 0:
            continue
        _, y0, _, y1 = _alpha_bbox(im)
        ch_raw = max(1, y1 - y0)
        ch_use = h if ch_raw < max(32, int(h * alpha_h_min_ratio)) else ch_raw
        scale = min(scale_cap, target_content_h / float(ch_use))
        nw = max(1, int(round(w * scale)))
        nh = max(1, int(round(h * scale)))
        scaled.append(im.resize((nw, nh), Image.Resampling.LANCZOS))
    if not scaled:
        raise RuntimeError("拼版失败：没有有效抠图")

    n = len(scaled)
    mw_ratio = max(0.5, min(1.0, float(max_width_ratio)))
    max_span = int(vi_w * mw_ratio)
    ov_r = max(0.0, min(0.35, float(overlap_ratio)))
    widths = [im.size[0] for im in scaled]
    mean_w = sum(widths) / max(len(widths), 1)
    overlap_px = int(round(mean_w * ov_r)) if n > 1 else 0

    def total_span_fn(images: list) -> int:
        if not images:
            return 0
        s = images[0].size[0]
        for j in range(1, len(images)):
            s += images[j].size[0] - overlap_px
        return s

    span = total_span_fn(scaled)
    if span > max_span and span > 0:
        factor = max_span / float(span)
        scaled = [
            im.resize(
                (max(1, int(im.size[0] * factor)), max(1, int(im.size[1] * factor))),
                Image.Resampling.LANCZOS,
            )
            for im in scaled
        ]
        widths = [im.size[0] for im in scaled]
        mean_w = sum(widths) / max(len(widths), 1)
        overlap_px = int(round(mean_w * ov_r)) if n > 1 else 0

    if n > 1 and overlap_px > 0:
        pair_caps = [
            min(scaled[i].size[0], scaled[i + 1].size[0]) - 1 for i in range(n - 1)
        ]
        overlap_px = min(overlap_px, max(0, min(pair_caps)))

    span = total_span_fn(scaled)

    gy = int(vi_h * max(0.55, min(0.94, float(ground_y_ratio))))
    st = max(0.0, min(0.08, float(y_stagger_ratio)))
    mid = (n - 1) / 2.0
    sm = (stagger_mode or "sides_up").strip().lower()
    if sm not in ("sides_up", "center_pyramid"):
        sm = "sides_up"
    max_st_px = int(round(vi_h * st))
    stagger_dys: list[int] = []
    if st <= 0:
        stagger_dys = [0] * n
    elif sm == "center_pyramid":
        max_d = max(abs(i - mid) for i in range(n)) or 1.0
        for i in range(n):
            dist = abs(i - mid)
            factor = 1.0 - (dist / max_d)
            stagger_dys.append(int(round(max_st_px * factor)))
    else:
        for i in range(n):
            dy = int(round(vi_h * st * abs(i - mid) / max(mid, 1e-6)))
            stagger_dys.append(dy)

    foot_bottoms: list[int] = []
    for im in scaled:
        _, _, _, y1a = _alpha_bbox(im)
        foot_bottoms.append(y1a)

    ys: list[int] = []
    for i, im in enumerate(scaled):
        y = gy - foot_bottoms[i] - stagger_dys[i]
        y = max(0, min(y, vi_h - im.size[1]))
        ys.append(sy0 + y)

    x0 = (vi_w - span) // 2
    xs: list[int] = []
    x = sx0 + x0
    for i, im in enumerate(scaled):
        xs.append(x)
        x += im.size[0] - (overlap_px if i < n - 1 else 0)

    canvas = _vertical_gradient_rgba(canvas_w, canvas_h, bg_top, bg_bottom)
    draw_order = list(range(n))
    if n == 3:
        draw_order = [0, 2, 1]
    elif n >= 3:
        mid_i = n // 2
        draw_order = [i for i in range(n) if i != mid_i] + [mid_i]

    for i in draw_order:
        canvas.alpha_composite(scaled[i], (xs[i], ys[i]))
    return canvas


def _detect_pre_cutout(image_path: Path, transparent_ratio_threshold: float = 0.25) -> bool:
    """自动检测图片是否已是干净抠图（含大量透明/半透明像素）。"""
    from PIL import Image

    try:
        im = Image.open(image_path)
    except Exception:
        return False
    if im.mode != "RGBA":
        return False
    try:
        import numpy as np
        arr = np.array(im, dtype=np.uint8)
        alpha = arr[:, :, 3]
        transparent = int(np.sum(alpha == 0))
        semi = int(np.sum((alpha > 0) & (alpha < 255)))
        total = int(alpha.size)
        ratio = (transparent + semi) / max(total, 1)
        return ratio >= transparent_ratio_threshold
    except ImportError:
        # 无 numpy 时逐像素检测（慢但兼容）
        alpha_data = im.getchannel("A")
        pixels = list(alpha_data.getdata())
        total = len(pixels)
        transparent = sum(1 for p in pixels if p == 0)
        semi = sum(1 for p in pixels if 0 < p < 255)
        return (transparent + semi) / max(total, 1) >= transparent_ratio_threshold


